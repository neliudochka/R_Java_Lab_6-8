package accessory;

/**
 * The {@code Cellophane} class represents a cellophane accessory that extends the {@code Accessory} class.
 * It sets the cost of the cellophane accessory upon creation using the provided cost value.
 */
public class Cellophane extends Accessory {

    /**
     * Constructs a {@code Cellophane} accessory with the specified cost.
     *
     * @param cost the cost of the cellophane accessory
     */
    public Cellophane(float cost) {
        super(cost);
    }
}
